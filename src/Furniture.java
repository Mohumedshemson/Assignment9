
public class Furniture {
	
	String name;
	float cost;
	String desctiption;
	float height;
	float width;
	float weight;
	
	public Furniture(String name, float cost, String desctiption, float height, float width, float weight) {
		super();
		this.name = name;
		this.cost = cost;
		this.desctiption = desctiption;
		this.height = height;
		this.width = width;
		this.weight = weight;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getCost() {
		return cost;
	}

	public void setCost(float cost) {
		this.cost = cost;
	}

	public String getDesctiption() {
		return desctiption;
	}

	public void setDesctiption(String desctiption) {
		this.desctiption = desctiption;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getWeight() {
		return weight;
	}

	public void setWeight(float weight) {
		this.weight = weight;
	}
	
	
	
	
	
	
	
}
